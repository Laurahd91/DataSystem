    private void boton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton1ActionPerformed
        // Creamos los objetos uduario e inicializamos la variable pass(password)
        UsuarioDAO uDAO = new UsuarioDAO();
        Usuario u = new Usuario();
        String pass = "";

        // Comprobamos que el Password introducido en ambos campos es igual
        if (txtPassword1.getText().equals(txtPassword2.getText())) {

            // Si se introduce la misma contrase√±a, guardamos la variable en el objeto usuario
            u.setPassword(txtPassword1.getText());

            try {
                // Realizamos la actualizaci√≥n en la base de datos llamando a la funci√≥n
                if (uDAO.actualizarPassword(u)) {
                    JOptionPane.showMessageDialog(this, "Actualizado correctamente");

                    // Inicializamos todos los JTextField
                    JTextField caja;
                    for (int i = 0; i < panelBorder1.getComponentCount(); i++) {
                        if (panelBorder1.getComponent(i).getClass().getName().equals("javax.swing.JTextField")) {
                            caja = (JTextField) panelBorder1.getComponent(i);
                            caja.setText(null);
                        }
                    }
                } else {
                    // Si no se puede actualizar, mostramos un mensaje de error
                    JOptionPane.showMessageDialog(this, "Ha ocurrido un error");
                }

            } catch (SQLException ex) {
                Logger.getLogger(InformacionCliente.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            // Mensaje que se muestra cuando las contrase√±as introducidas no coinciden
            JOptionPane.showMessageDialog(this, "Las contrase√±as no coinciden");
            // Inicializamos todos los JTextField
            JTextField caja;
            for (int i = 0; i < panelBorder1.getComponentCount(); i++) {
                if (panelBorder1.getComponent(i).getClass().getName().equals("javax.swing.JTextField")) {
                    caja = (JTextField) panelBorder1.getComponent(i);
                    caja.setText(null);
                }
            }
        }


    }//GEN-LAST:event_boton1ActionPerformed

